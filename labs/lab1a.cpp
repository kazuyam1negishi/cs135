#include <iostream>
using namespace std;

int main()
{
    int addedFormula = 5 + 2;
    int subtractedFormula = 5 - 2;
    int multipliedFormula = 5 * 2;
    int dividedFormula = 5 / 2;
    int modulusFormula = 5 % 2;
    double dividedPercent = 5.0 / 2.0;
    cout << "5 + 2 = " << addedFormula << endl;
    cout << "5 - 2 = " << subtractedFormula << endl;
    cout << "5 * 2 = " << multipliedFormula << endl;
    cout << "5 / 2 = " << dividedFormula << endl;
    cout << "5 % 2 = " << modulusFormula << endl ;
    cout << "5.0 / 2.0 = " << dividedPercent <<endl;
}